package com.erdemserhat.rsa;

/**
 * Enum for specifying the range of randomly generated numbers.
 */
public enum RandomNumberGetterLevel {
    /**
     * Represents a range between 500 and 1,000.
     */
    LEVEL_1,

    /**
     * Represents a range between 1,000 and 2,000.
     */
    LEVEL_2,

    /**
     * Represents a range between 5,000 and 10,000.
     */
    LEVEL_3,

    /**
     * Represents a range between 20,000 and 30,000.
     */
    LEVEL_4
}
